func TestClassify_AllTypeFamilies(t *testing.T) {
	tests := []struct {
		subject string
		bucket  string
		want    string
	}{
		{"feat: add new thing", "Added", "add new thing"},
		{"feature: another", "Added", "another"},
		{"fix: bug", "Fixed", "bug"},
		{"bugfix: another bug", "Fixed", "another bug"},
		{"refactor: cleanup", "Changed", "cleanup"},
		{"perf: optimize", "Changed", "optimize"},
		{"performance: hot path", "Changed", "hot path"},
		{"stability: race fix", "Changed", "race fix"},
		{"techdebt: cleanup", "Changed", "cleanup"},
		{"docs: explain it", "Docs", "explain it"},
		{"documentation: longer", "Docs", "longer"},
		{"doc: short", "Docs", "short"},
		{"feat(v11.6.5): scoped", "Added", "scoped"},
		{"fix(scope): scoped fix", "Fixed", "scoped fix"},
		{"FEAT: uppercase", "Added", "uppercase"},
		{"Feat: titlecase", "Added", "titlecase"},
	}
	for _, tc := range tests {
		t.Run(tc.subject, func(t *testing.T) {
			var b Buckets
			Classify(tc.subject, &b)
			var actual []string
			switch tc.bucket {
			case "Added":
				actual = b.Added
			case "Fixed":
				actual = b.Fixed
			case "Changed":
				actual = b.Changed
			case "Docs":
				actual = b.Docs
			case "Other":
				actual = b.Other
			}
			if len(actual) != 1 || actual[0] != tc.want {
				t.Errorf("got %v, want [%q] in %s", actual, tc.want, tc.bucket)
			}
		})
	}
}

