func TestWriteEntry_FreshFile(t *testing.T) {
	tmp := t.TempDir()
	cl := filepath.Join(tmp, "CHANGELOG.md")
	entry := "## [1.0.0]\n\n- x\n\n---\n\n"
	wrote, skipped, err := WriteEntry(cl, "1.0.0", entry)
	if err != nil {
		t.Fatalf("%v", err)
	}
	if !wrote || skipped {
		t.Errorf("wrote=%v skipped=%v, want true/false", wrote, skipped)
	}
	body, _ := os.ReadFile(cl)
	if !strings.Contains(string(body), DefaultFreshContent) {
		t.Errorf("missing default header: %s", body)
	}
	if !strings.Contains(string(body), entry) {
		t.Errorf("missing entry: %s", body)
	}
}

func TestWriteEntry_Idempotent(t *testing.T) {
	tmp := t.TempDir()
	cl := filepath.Join(tmp, "CHANGELOG.md")
	seed := "# Changelog\n\n## [1.0.0]\n\nfirst run\n\n---\n\n"
	_ = os.WriteFile(cl, []byte(seed), 0o644)

	entry := "## [1.0.0]\n\n- different but version matches\n\n---\n\n"
	wrote, skipped, err := WriteEntry(cl, "1.0.0", entry)
	if err != nil {
		t.Fatalf("%v", err)
	}
	if wrote || !skipped {
		t.Errorf("wrote=%v skipped=%v, want false/true", wrote, skipped)
	}
	// Original content unchanged.
	body, _ := os.ReadFile(cl)
	if string(body) != seed {
		t.Errorf("body mutated: %s", body)
	}
}

func TestWriteEntry_PrependPreservesHeader(t *testing.T) {
	tmp := t.TempDir()
	cl := filepath.Join(tmp, "CHANGELOG.md")
	seed := "# Changelog\n\nIntro line.\n\n## [1.0.0]\n\nfirst run\n\n---\n\n"
	_ = os.WriteFile(cl, []byte(seed), 0o644)

	entry := "## [1.1.0]\n\n- new\n\n---\n\n"
	wrote, skipped, err := WriteEntry(cl, "1.1.0", entry)
	if err != nil {
		t.Fatalf("%v", err)
	}
	if !wrote || skipped {
		t.Errorf("expected fresh write")
	}
	body, _ := os.ReadFile(cl)
	if !strings.Contains(string(body), "Intro line.\n\n## [1.1.0]") {
		t.Errorf("header not preserved: %s", body)
	}
}

